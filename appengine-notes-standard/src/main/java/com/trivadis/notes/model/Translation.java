package com.trivadis.notes.model;

public class Translation {
	private String textOriginal;
	private String langOriginal;
	private String textTranslated;
	private String langTranslated;
	public String getTextOriginal() {
		return textOriginal;
	}
	public void setTextOriginal(String textOriginal) {
		this.textOriginal = textOriginal;
	}
	public String getLangOriginal() {
		return langOriginal;
	}
	public void setLangOriginal(String langOriginal) {
		this.langOriginal = langOriginal;
	}
	public String getTextTranslated() {
		return textTranslated;
	}
	public void setTextTranslated(String textTranslated) {
		this.textTranslated = textTranslated;
	}
	public String getLangTranslated() {
		return langTranslated;
	}
	public void setLangTranslated(String langTranslated) {
		this.langTranslated = langTranslated;
	}

}
